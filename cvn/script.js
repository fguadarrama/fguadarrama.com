const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => Array.from(document.querySelectorAll(selector));
let currentLang = 'en';

function setText(lang) {
  const dictionary = content[lang];
  $$('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });
}

function itemTemplate(item, index) {
  return `
    <article class="item reveal" style="animation-delay:${index * 42}ms">
      <div class="item-header">
        <p class="meta">${item.date}</p>
        <div>
          <h3 class="item-title">${item.title}</h3>
          <p class="place">${item.place}</p>
          <p class="location">${item.location}</p>
        </div>
      </div>
      <p class="details">${item.details}</p>
    </article>
  `;
}

function publicationTemplate(pub, index) {
  const inner = `
    <span class="tag">${pub.tag}</span>
    <h3 class="item-title">${pub.title}</h3>
    <p class="citation">${pub.citation}</p>
  `;

  return `
    <article class="item publication reveal" style="animation-delay:${index * 42}ms">
      ${pub.url ? `<a href="${pub.url}" target="_blank" rel="noopener">${inner}</a>` : inner}
    </article>
  `;
}

function render(lang = currentLang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  setText(lang);
  $('#experience-list').innerHTML = content[lang].experience.map(itemTemplate).join('');
  $('#education-list').innerHTML = content[lang].education.map(itemTemplate).join('');
  $('#publication-list').innerHTML = content[lang].publications.map(publicationTemplate).join('');
  $$('[data-lang]').forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle('active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });
}

$$('[data-lang]').forEach((button) => {
  button.addEventListener('click', () => render(button.dataset.lang));
});

$('#top-button').addEventListener('click', () => {
  window.scrollTo({ top: 0, behavior: 'smooth' });
});

$('#contact-form').addEventListener('submit', (event) => {
  event.preventDefault();
  const form = event.currentTarget;
  const status = $('#form-status');
  const email = form.email.value.trim();
  const message = form.message.value.trim();
  const validEmail = /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
  const validMessage = message.length >= 4;

  status.textContent = validEmail && validMessage
    ? content[currentLang].formSuccess
    : content[currentLang].formMissing;
});

render(currentLang);
